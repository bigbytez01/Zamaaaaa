# รับค่าตัวเลขจากผู้ใช้
num1 = float(input("ป้อนตัวเลขตัวตั้ง: "))
num2 = float(input("ป้อนตัวเลขตัวหาร: "))

# ตรวจสอบว่าตัวหารไม่ใช่ศูนย์
if num2 != 0:
    quotient = num1 / num2
    print("ผลหารของ", num1, "ด้วย", num2, "คือ", quotient)
else:
    print("ไม่สามารถหารด้วยศูนย์ได้ครับ กรุณาป้อนตัวเลขใหม่ที่ไม่เป็นศูนย์")